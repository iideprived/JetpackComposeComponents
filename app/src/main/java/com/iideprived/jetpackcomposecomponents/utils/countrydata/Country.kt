            it.country.contains(code,true) || code.contains(it.country, true) ||
            it.name.contains(code, true) || code.contains(it.name, true) ||
            it.flagEmoji.contains(code, true) || code.contains(it.flagEmoji, true)
        }

        fun getFromLocale(default: Country = US) : Country = fromCountryCode(Locale.getDefault().country) ?: default

