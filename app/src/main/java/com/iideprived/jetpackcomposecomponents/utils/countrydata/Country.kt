        fun searchByAbbreviation(code: String, list : List<Country> = Country.values().toList()) : List<Country> = list.filter { it.name.contains(code, true) || code.contains(it.name, true) }
        fun searchAll(code: String, list : List<Country> = Country.values().toList()) : List<Country> = list.filter {
